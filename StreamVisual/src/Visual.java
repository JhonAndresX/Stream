    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtNombre.getText().equals("") || txtApellido1.getText().equals("") || txtApellido2.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "LLENE TODOS LOS DATOS");
        } else {
            lista();

            String nombre = txtNombre.getText();
            String nombreA = Character.toUpperCase(nombre.charAt(0)) + nombre.substring(1).toLowerCase();
            String apellido1 = txtApellido1.getText().toLowerCase();
            String Apellidoa1 = Character.toUpperCase(apellido1.charAt(0)) + apellido1.substring(1).toLowerCase();
            String apellido2 = txtApellido2.getText().toLowerCase();
            String Apellidoa2 = Character.toUpperCase(apellido2.charAt(0)) + apellido2.substring(1).toLowerCase();

            Optional<Persona> personaEncontrada = personas.stream().filter(persona -> persona.getNombre().equals(nombreA) && persona.getApellido1().equals(Apellidoa1)
                    && persona.getApellido2().equals(Apellidoa2)).findFirst();

            personaEncontrada.ifPresentOrElse(persona -> resultado.setText("SI EXISTE: " + persona.getNombre() + " " + persona.getApellido1() + " "
                    + persona.getApellido2()), () -> resultado.setText("NO EXISTE ESA PERSONA"));
            long inicio = System.currentTimeMillis();
// aquí va el proceso que deseas medir
            long fin = System.currentTimeMillis();
            long tiempo = fin - inicio;
            System.out.println("Tiempo de ejecución: " + tiempo + " milisegundos");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

